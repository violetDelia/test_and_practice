#include <string.h>
#include <unordered_map>
#include <unordered_set>
#include <vector>
using namespace std;
class Solution
{
public:
    int titleToNumber(string columnTitle)
    {
        long long res = 0;
        long long base = 1;
        for (int i = columnTitle.size() - 1; i >= 0; --i)
        {
            res += base * (columnTitle[i] - 'A' + 1);
            base *= 26;
        }
        if (res > INT_MAX)
            return INT_MAX;
        return (int)res;
    }
};
